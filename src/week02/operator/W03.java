package week02.operator;

public class W03 {
    public static void main(String[] args) {
        // 비교연산자
        System.out.println(10>9); // 10이 9보다 크니? true
        System.out.println(10<9);
        System.out.println(10>=9);
        System.out.println(10<=9);
        System.out.println(10==9);
        System.out.println(10==10);
        System.out.println(10 != 10);
        System.out.println(10 != 9);

    }
}
